export function zerosFormatter(value, valueLength) {
  let valueString = value.toString();

  if (valueString.length !== valueLength) {
    const length = valueLength - valueString.length;
    for (let i = 0; i < length; i++) {
      valueString = `0${valueString}`;
    }
  }
  return valueString;
}

export function getTimeFormat(dotnettick) {
  if (dotnettick !== ' ') {
    const date = new Date((dotnettick - 621355968000000000) / 10000);
    let hours = date.getHours();
    const suffix = hours >= 12 ? 'PM' : 'AM';

    hours = (hours > 12) ? hours - 12 : hours;
    hours = hours === '00' ? 12 : hours;
    return `${date.getFullYear()}-${zerosFormatter((date.getMonth() + 1), 2)}-${zerosFormatter(date.getDate(), 2)} ${zerosFormatter(hours, 2)}:${zerosFormatter(date.getMinutes(), 2)}:${zerosFormatter(date.getSeconds(), 2)}.${zerosFormatter(date.getMilliseconds(), 3)} ${suffix}`; // eslint-disable-line max-len
  }

  return dotnettick;
}

export function getTimeFormatEpoch(epoch) {
  if (epoch !== ' ') {
    const date = new Date((epoch));
    let hours = date.getHours();
    const suffix = (hours >= 12) ? 'PM' : 'AM';

    hours = (hours > 12) ? hours - 12 : hours;
    hours = hours === '00' ? 12 : hours;
    return `${date.getFullYear()}-${zerosFormatter((date.getMonth() + 1), 2)}-${zerosFormatter(date.getDate(), 2)} ${zerosFormatter(hours, 2)}:${zerosFormatter(date.getMinutes(), 2)} ${suffix}`; // eslint-disable-line max-len
  }

  return epoch;
}


export function updateArrayByPx(array, trade) {
  let current = null;

  array.forEach((obj, i) => {
    if (obj.Price === trade.Price) {
      if (!trade.Quantity) {
        array.splice(i, 1);
      } else {
        // updating the quantity on an order
        obj.Quantity = trade.Quantity; // eslint-disable-line no-param-reassign
        current = obj;
      }
    }
  });

  if (!current && trade.Quantity) array.push(trade);
  return array;
}

export function toCamelCase(str) {
  return str.replace(/-([a-z])/g, (g) => g[1].toUpperCase());
}

export function type(variable) {
  return Object.prototype.toString.call(variable).replace('[object ', '').replace(']', '').toLowerCase();
}

